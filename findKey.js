//function should return the key of the first value to match the callback
//loop through the objects, if statement to compare callback against value
//if match, return the key at that position
const findKey = function(object, callback){
  let output = undefined
  for (let i in object) {

    if (callback(object[i])) {
      output = i
      break
    }
 
  }
  return output
}



